const db = require('../Config/Sequelize')
const Invoice = db.InvoiceModel

module.exports = {

    addInvoice: (req, res) => {
        if (!req.body.name) {
            res.status(400).send({ message: "Invoice Name Can not be Emapty" })
            return;
        }
        const data = req.body;
        Invoice.create(data).then(data => {
            res.send(data);
        }).catch(err => {
            res.status(500).send({
                message:
                    err.message || "Some error occurred while creating the Invoice."
            });
        });
    },
    getAllInvoice: (req, res) => {
        Invoice.findAll().then(result => {
            if (result) {
                res.json({
                    success: 1,
                    message: "Data Recived",
                    data: result
                })
            } else {
                res.json({
                    success: 0,
                    message: "Fail Recived"
                })
            }
        })
    },
    getInvoiceById: (req, res) => {
        let id = req.query.id
        Invoice.findByPk(id).then(result => {
            if (result) {
                res.json({
                    success: 1,
                    message: "Data Recived",
                    data: result
                })
            } else {
                res.json({
                    success: 0,
                    message: "Fail Recived"
                })
            }
        })
    },
    updateInvoice: (req, res) => {
        let id = req.query.id
        let data = req.body;
        Invoice.update(data, {
            where: { id: id }
        }).then(result => {
            if (result) {
                res.json({
                    success: 1,
                    message: "Data Updated",
                    data: result
                })
            } else {
                res.json({
                    success: 0,
                    message: "Fail To Updated"
                })
            }
        })
    },
    updateInvoiceStatus: (req, res) => {
        let id = req.query.id
        let status = req.params;
        Invoice.update({ status: status }, {
            where: { id: id }
        }).then(result => {
            if (result) {
                res.json({
                    success: 1,
                    message: "Data Updated",
                    data: result
                })
            } else {
                res.json({
                    success: 0,
                    message: "Fail To Updated"
                })
            }
        })
    },
    deleteInvoiceById: (req, res) => {
        let id = req.query.id
        Invoice.destroy({ where: { id: id } }).then(result => {
            if (result) {
                res.json({
                    success: 1,
                    message: "Data Deleted",
                    data: result
                })
            } else {
                res.json({
                    success: 0,
                    message: "Fail To Deleted"
                })
            }
        })
    }
}