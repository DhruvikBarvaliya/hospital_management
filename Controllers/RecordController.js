const db = require('../Config/Sequelize')
const Record = db.RecordModel

module.exports = {

    addRecord: (req, res) => {
        if (!req.body.name) {
            res.status(400).send({ message: "Record Name Can not be Emapty" })
            return;
        }
        const data = req.body;
        Record.create(data).then(data => {
            res.send(data);
        }).catch(err => {
            res.status(500).send({
                message:
                    err.message || "Some error occurred while creating the Record."
            });
        });
    },
    getAllRecord: (req, res) => {
        Record.findAll().then(result => {
            if (result) {
                res.json({
                    success: 1,
                    message: "Data Recived",
                    data: result
                })
            } else {
                res.json({
                    success: 0,
                    message: "Fail Recived"
                })
            }
        })
    },
    getRecordById: (req, res) => {
        let id = req.query.id
        Record.findByPk(id).then(result => {
            if (result) {
                res.json({
                    success: 1,
                    message: "Data Recived",
                    data: result
                })
            } else {
                res.json({
                    success: 0,
                    message: "Fail Recived"
                })
            }
        })
    },
    updateRecord: (req, res) => {
        let id = req.query.id
        let data = req.body;
        Record.update(data, {
            where: { id: id }
        }).then(result => {
            if (result) {
                res.json({
                    success: 1,
                    message: "Data Updated",
                    data: result
                })
            } else {
                res.json({
                    success: 0,
                    message: "Fail To Updated"
                })
            }
        })
    },
    updateRecordStatus: (req, res) => {
        let id = req.query.id
        let status = req.params;
        Record.update({ status: status }, {
            where: { id: id }
        }).then(result => {
            if (result) {
                res.json({
                    success: 1,
                    message: "Data Updated",
                    data: result
                })
            } else {
                res.json({
                    success: 0,
                    message: "Fail To Updated"
                })
            }
        })
    },
    deleteRecordById: (req, res) => {
        let id = req.query.id
        Record.destroy({ where: { id: id } }).then(result => {
            if (result) {
                res.json({
                    success: 1,
                    message: "Data Deleted",
                    data: result
                })
            } else {
                res.json({
                    success: 0,
                    message: "Fail To Deleted"
                })
            }
        })
    }
}