const db = require('../Config/Sequelize')
const Doctor = db.DoctorModel

module.exports = {

    addDoctor: (req, res) => {
        if (!req.body.name) {
            res.status(400).send({ message: "Doctor Name Can not be Emapty" })
            return;
        }
        const data = req.body;
        Doctor.create(data).then(data => {
            res.send(data);
        }).catch(err => {
            res.status(500).send({
                message:
                    err.message || "Some error occurred while creating the Doctor."
            });
        });
    },
    getAllDoctor: (req, res) => {
        Doctor.findAll().then(result => {
            if (result) {
                res.json({
                    success: 1,
                    message: "Data Recived",
                    data: result
                })
            } else {
                res.json({
                    success: 0,
                    message: "Fail Recived"
                })
            }
        })
    },
    getDoctorById: (req, res) => {
        let id = req.query.id
        Doctor.findByPk(id).then(result => {
            if (result) {
                res.json({
                    success: 1,
                    message: "Data Recived",
                    data: result
                })
            } else {
                res.json({
                    success: 0,
                    message: "Fail Recived"
                })
            }
        })
    },
    updateDoctor: (req, res) => {
        let id = req.query.id
        let data = req.body;
        Doctor.update(data, {
            where: { id: id }
        }).then(result => {
            if (result) {
                res.json({
                    success: 1,
                    message: "Data Updated",
                    data: result
                })
            } else {
                res.json({
                    success: 0,
                    message: "Fail To Updated"
                })
            }
        })
    },
    updateDoctorStatus: (req, res) => {
        let id = req.query.id
        let status = req.params;
        Doctor.update({ status: status }, {
            where: { id: id }
        }).then(result => {
            if (result) {
                res.json({
                    success: 1,
                    message: "Data Updated",
                    data: result
                })
            } else {
                res.json({
                    success: 0,
                    message: "Fail To Updated"
                })
            }
        })
    },
    deleteDoctorById: (req, res) => {
        let id = req.query.id
        Doctor.destroy({ where: { id: id } }).then(result => {
            if (result) {
                res.json({
                    success: 1,
                    message: "Data Deleted",
                    data: result
                })
            } else {
                res.json({
                    success: 0,
                    message: "Fail To Deleted"
                })
            }
        })
    }
}