const db = require('../Config/Sequelize')
const Patient = db.PatientModel

module.exports = {

    addPatient: (req, res) => {
        if (!req.body.patient_first_name) {
            res.status(400).send({ message: "Patient Name Can not be Emapty" })
            return;
        }
        const data = req.body;
        Patient.create(data).then(data => {
            res.send(data);
        }).catch(err => {
            res.status(500).send({
                message:
                    err.message || "Some error occurred while creating the Patient."
            });
        });
    },
    getAllPatient: (req, res) => {
        Patient.findAll().then(result => {
            if (result) {
                res.json({
                    success: 1,
                    message: "Data Recived",
                    data: result
                })
            } else {
                res.json({
                    success: 0,
                    message: "Fail Recived"
                })
            }
        })
    },
    getPatientById: (req, res) => {
        let id = req.query.id
        Patient.findByPk(id).then(result => {
            if (result) {
                res.json({
                    success: 1,
                    message: "Data Recived",
                    data: result
                })
            } else {
                res.json({
                    success: 0,
                    message: "Fail Recived"
                })
            }
        })
    },
    updatePatient: (req, res) => {
        let id = req.query.id
        let data = req.body;
        Patient.update(data, {
            where: { id: id }
        }).then(result => {
            if (result) {
                res.json({
                    success: 1,
                    message: "Data Updated",
                    data: result
                })
            } else {
                res.json({
                    success: 0,
                    message: "Fail To Updated"
                })
            }
        })
    },
    updatePatientStatus: (req, res) => {
        let id = req.query.id
        let status = req.params;
        Patient.update({ status: status }, {
            where: { id: id }
        }).then(result => {
            if (result) {
                res.json({
                    success: 1,
                    message: "Data Updated",
                    data: result
                })
            } else {
                res.json({
                    success: 0,
                    message: "Fail To Updated"
                })
            }
        })
    },
    deletePatientById: (req, res) => {
        let id = req.query.id
        Patient.destroy({ where: { id: id } }).then(result => {
            if (result) {
                res.json({
                    success: 1,
                    message: "Data Deleted",
                    data: result
                })
            } else {
                res.json({
                    success: 0,
                    message: "Fail To Deleted"
                })
            }
        })
    }
}